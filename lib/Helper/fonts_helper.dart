import 'package:flutter/material.dart';
import 'package:flutter_screenutil/src/size_extension.dart';

TextStyle titleBold({double size = 16, required Color color}) {
  return TextStyle(
    fontFamily: "",
    fontWeight: FontWeight.w600,
    fontSize: size.sp,

    overflow: TextOverflow.ellipsis,
    color: color,
  );
}

TextStyle titleNormal({double size = 16, required Color color}) {
  return TextStyle(
    fontFamily: "sanaiey",
    fontWeight: FontWeight.normal,
    overflow: TextOverflow.ellipsis,
